package com.example.demo.model;

public class Sum {


    int x;
    int y;
    int result;

    public Sum(int x, int y) {
        this.x = x;
        this.y = y;
        this.result = x+y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getResult() {
        return result;
    }




}
